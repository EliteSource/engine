package net.elitesource.gengine.utils;

public class Location
{

	protected float x, y;

	public Location(float x, float y)
	{
		super();
		this.x = x;
		this.y = y;
	}

	public float getX()
	{
		return x;
	}

	public void setX(float x)
	{
		this.x = x;
	}

	public float getY()
	{
		return y;
	}

	public void setY(float y)
	{
		this.y = y;
	}

}
